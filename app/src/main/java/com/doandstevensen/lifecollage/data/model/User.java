public class User {
    private int uid;
    private String username;

    public void setUid(int uid) {
        this.uid = uid;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public int getUid() {
        return uid;
    }

    public String getUsername() {
        return username;
    }


}
